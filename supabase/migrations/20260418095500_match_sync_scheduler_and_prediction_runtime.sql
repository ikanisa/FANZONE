-- Live match sync runtime hardening:
-- 1) Normalize public.matches.status across all writers.
-- 2) Add DB-level duplicate protection for live_match_events.
-- 3) Add scheduler state + request logs for observability/backoff.
-- 4) Dispatch cron-driven calls into gemini-sports-data.

CREATE OR REPLACE FUNCTION public.normalize_match_status_value(input_status text)
RETURNS text
LANGUAGE plpgsql
IMMUTABLE
AS $$
DECLARE
  normalized text := lower(regexp_replace(coalesce(input_status, ''), '[\s-]+', '_', 'g'));
BEGIN
  CASE normalized
    WHEN 'live', 'in_play', 'inprogress', 'in_progress', 'playing' THEN
      RETURN 'live';
    WHEN 'finished', 'complete', 'completed', 'full_time', 'ft' THEN
      RETURN 'finished';
    WHEN 'scheduled', 'not_started', 'notstarted', 'pending', 'upcoming' THEN
      RETURN 'upcoming';
    WHEN 'postponed' THEN
      RETURN 'postponed';
    WHEN 'cancelled', 'canceled' THEN
      RETURN 'cancelled';
    ELSE
      RETURN NULLIF(normalized, '');
  END CASE;
END;
$$;

CREATE OR REPLACE FUNCTION public.normalize_match_status_before_write()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.status := coalesce(
    public.normalize_match_status_value(NEW.status),
    lower(coalesce(NEW.status, 'upcoming'))
  );
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS normalize_match_status_before_write ON public.matches;
CREATE TRIGGER normalize_match_status_before_write
BEFORE INSERT OR UPDATE OF status
ON public.matches
FOR EACH ROW
EXECUTE FUNCTION public.normalize_match_status_before_write();

UPDATE public.matches
SET status = public.normalize_match_status_value(status)
WHERE status IS NOT NULL
  AND status <> public.normalize_match_status_value(status);

ALTER TABLE public.live_match_events
ADD COLUMN IF NOT EXISTS event_signature text
GENERATED ALWAYS AS (
  md5(
    coalesce(minute::text, '') || '|' ||
    coalesce(event_type, '') || '|' ||
    lower(coalesce(team, '')) || '|' ||
    lower(coalesce(player, '')) || '|' ||
    lower(coalesce(details, ''))
  )
) STORED;

CREATE UNIQUE INDEX IF NOT EXISTS live_match_events_match_signature_idx
ON public.live_match_events (match_id, event_signature);

CREATE TABLE IF NOT EXISTS public.match_sync_state (
  match_id text PRIMARY KEY REFERENCES public.matches(id) ON DELETE CASCADE,
  last_events_refresh_at timestamptz,
  last_odds_refresh_at timestamptz,
  last_events_status text,
  last_odds_status text,
  last_events_http_status integer,
  last_odds_http_status integer,
  last_events_error text,
  last_odds_error text,
  consecutive_event_failures integer NOT NULL DEFAULT 0,
  consecutive_odds_failures integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT timezone('utc', now()),
  updated_at timestamptz NOT NULL DEFAULT timezone('utc', now())
);

CREATE TABLE IF NOT EXISTS public.match_sync_request_log (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id text REFERENCES public.matches(id) ON DELETE CASCADE,
  fetch_type text NOT NULL CHECK (fetch_type IN ('events', 'odds')),
  request_id bigint,
  request_payload jsonb NOT NULL,
  response_status integer,
  success boolean NOT NULL DEFAULT false,
  response_body jsonb,
  error_text text,
  requested_at timestamptz NOT NULL DEFAULT timezone('utc', now()),
  completed_at timestamptz
);

CREATE INDEX IF NOT EXISTS match_sync_request_log_match_id_idx
ON public.match_sync_request_log (match_id, requested_at DESC);

CREATE INDEX IF NOT EXISTS match_sync_request_log_fetch_type_idx
ON public.match_sync_request_log (fetch_type, requested_at DESC);

CREATE OR REPLACE FUNCTION public.dispatch_match_sync_jobs(
  p_fetch_type text DEFAULT 'events',
  p_limit integer DEFAULT 12
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $$
DECLARE
  v_now timestamptz := timezone('utc', now());
  v_limit integer := greatest(coalesce(p_limit, 12), 1);
  v_project_url text;
  v_anon_key text;
  v_admin_secret text;
  v_request_id bigint;
  v_http_result net.http_response_result;
  v_http_status integer;
  v_response_body jsonb;
  v_success boolean;
  v_error_text text;
  v_dispatched integer := 0;
  v_succeeded integer := 0;
  v_failed integer := 0;
  rec record;
BEGIN
  IF p_fetch_type NOT IN ('events', 'odds') THEN
    RAISE EXCEPTION 'Unsupported fetch type: %', p_fetch_type;
  END IF;

  SELECT
    coalesce(
      (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'match_sync_project_url'),
      (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'openfootball_sync_project_url')
    ),
    coalesce(
      (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'match_sync_anon_key'),
      (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'openfootball_sync_anon_key')
    ),
    (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'match_sync_admin_secret')
  INTO v_project_url, v_anon_key, v_admin_secret;

  IF v_project_url IS NULL OR v_anon_key IS NULL OR v_admin_secret IS NULL THEN
    RAISE EXCEPTION 'Missing vault secrets for match sync dispatcher.';
  END IF;

  FOR rec IN
    WITH candidates AS (
      SELECT
        m.id,
        m.home_team,
        m.away_team,
        public.normalize_match_status_value(m.status) AS normalized_status,
        (m.date::timestamp + coalesce(m.kickoff_time, time '00:00')) AS kickoff_at,
        s.last_events_refresh_at,
        s.last_odds_refresh_at,
        coalesce(s.consecutive_event_failures, 0) AS consecutive_event_failures,
        coalesce(s.consecutive_odds_failures, 0) AS consecutive_odds_failures
      FROM public.matches m
      LEFT JOIN public.match_sync_state s ON s.match_id = m.id
      WHERE m.date BETWEEN current_date AND current_date + 1
        AND (
          CASE
            WHEN p_fetch_type = 'events' THEN
              (
                public.normalize_match_status_value(m.status) = 'live'
                AND coalesce(s.last_events_refresh_at, 'epoch'::timestamptz) <=
                  v_now - CASE
                    WHEN coalesce(s.consecutive_event_failures, 0) >= 3 THEN interval '10 minutes'
                    ELSE interval '2 minutes'
                  END
              )
              OR
              (
                public.normalize_match_status_value(m.status) = 'upcoming'
                AND (m.date::timestamp + coalesce(m.kickoff_time, time '00:00')) <= v_now + interval '4 hours'
                AND coalesce(s.last_events_refresh_at, 'epoch'::timestamptz) <=
                  v_now - CASE
                    WHEN coalesce(s.consecutive_event_failures, 0) >= 3 THEN interval '30 minutes'
                    ELSE interval '10 minutes'
                  END
              )
            WHEN p_fetch_type = 'odds' THEN
              public.normalize_match_status_value(m.status) IN ('upcoming', 'live')
              AND (m.date::timestamp + coalesce(m.kickoff_time, time '00:00')) BETWEEN
                v_now - interval '2 hours' AND v_now + interval '36 hours'
              AND coalesce(s.last_odds_refresh_at, 'epoch'::timestamptz) <=
                v_now - CASE
                  WHEN public.normalize_match_status_value(m.status) = 'live' THEN interval '10 minutes'
                  WHEN coalesce(s.consecutive_odds_failures, 0) >= 3 THEN interval '60 minutes'
                  ELSE interval '30 minutes'
                END
            ELSE false
          END
        )
      ORDER BY
        CASE WHEN public.normalize_match_status_value(m.status) = 'live' THEN 0 ELSE 1 END,
        kickoff_at ASC
      LIMIT v_limit
    )
    SELECT * FROM candidates
  LOOP
    v_dispatched := v_dispatched + 1;

    BEGIN
      v_request_id := net.http_post(
        url := v_project_url || '/functions/v1/gemini-sports-data',
        headers := jsonb_build_object(
          'Content-Type', 'application/json',
          'Authorization', 'Bearer ' || v_anon_key,
          'apikey', v_anon_key,
          'x-match-sync-secret', v_admin_secret
        ),
        body := jsonb_build_object(
          'teamA', rec.home_team,
          'teamB', rec.away_team,
          'matchId', rec.id,
          'fetchType', p_fetch_type
        )
      );

      SELECT * INTO v_http_result
      FROM net.http_collect_response(v_request_id, false);

      v_http_status := v_http_result.response.status_code;

      BEGIN
        v_response_body := CASE
          WHEN nullif(trim(coalesce(v_http_result.response.content, '')), '') IS NULL THEN NULL
          ELSE v_http_result.response.content::jsonb
        END;
      EXCEPTION WHEN others THEN
        v_response_body := jsonb_build_object(
          'raw', coalesce(v_http_result.response.content, '')
        );
      END;

      v_success := coalesce(v_http_status BETWEEN 200 AND 299, false)
        AND coalesce((v_response_body->>'success')::boolean, false);
      v_error_text := CASE
        WHEN v_success THEN NULL
        ELSE coalesce(
          v_response_body->>'error',
          v_http_result.response.error_msg,
          v_http_result.message,
          'Unknown match sync error'
        )
      END;
    EXCEPTION WHEN others THEN
      v_http_status := NULL;
      v_response_body := NULL;
      v_success := false;
      v_error_text := SQLERRM;
    END;

    INSERT INTO public.match_sync_request_log (
      match_id,
      fetch_type,
      request_id,
      request_payload,
      response_status,
      success,
      response_body,
      error_text,
      requested_at,
      completed_at
    )
    VALUES (
      rec.id,
      p_fetch_type,
      v_request_id,
      jsonb_build_object(
        'teamA', rec.home_team,
        'teamB', rec.away_team,
        'matchId', rec.id,
        'fetchType', p_fetch_type
      ),
      v_http_status,
      v_success,
      v_response_body,
      v_error_text,
      v_now,
      timezone('utc', now())
    );

    IF p_fetch_type = 'events' THEN
      INSERT INTO public.match_sync_state (
        match_id,
        last_events_refresh_at,
        last_events_status,
        last_events_http_status,
        last_events_error,
        consecutive_event_failures,
        updated_at
      )
      VALUES (
        rec.id,
        v_now,
        CASE
          WHEN v_success THEN coalesce(v_response_body #>> '{currentState,match_status}', rec.normalized_status)
          ELSE NULL
        END,
        v_http_status,
        CASE WHEN v_success THEN NULL ELSE v_error_text END,
        CASE WHEN v_success THEN 0 ELSE 1 END,
        timezone('utc', now())
      )
      ON CONFLICT (match_id) DO UPDATE
      SET
        last_events_refresh_at = excluded.last_events_refresh_at,
        last_events_status = coalesce(excluded.last_events_status, public.match_sync_state.last_events_status),
        last_events_http_status = excluded.last_events_http_status,
        last_events_error = excluded.last_events_error,
        consecutive_event_failures = CASE
          WHEN excluded.last_events_error IS NULL THEN 0
          ELSE public.match_sync_state.consecutive_event_failures + 1
        END,
        updated_at = excluded.updated_at;
    ELSE
      INSERT INTO public.match_sync_state (
        match_id,
        last_odds_refresh_at,
        last_odds_status,
        last_odds_http_status,
        last_odds_error,
        consecutive_odds_failures,
        updated_at
      )
      VALUES (
        rec.id,
        v_now,
        CASE
          WHEN v_success THEN 'ok'
          ELSE NULL
        END,
        v_http_status,
        CASE WHEN v_success THEN NULL ELSE v_error_text END,
        CASE WHEN v_success THEN 0 ELSE 1 END,
        timezone('utc', now())
      )
      ON CONFLICT (match_id) DO UPDATE
      SET
        last_odds_refresh_at = excluded.last_odds_refresh_at,
        last_odds_status = coalesce(excluded.last_odds_status, public.match_sync_state.last_odds_status),
        last_odds_http_status = excluded.last_odds_http_status,
        last_odds_error = excluded.last_odds_error,
        consecutive_odds_failures = CASE
          WHEN excluded.last_odds_error IS NULL THEN 0
          ELSE public.match_sync_state.consecutive_odds_failures + 1
        END,
        updated_at = excluded.updated_at;
    END IF;

    IF v_success THEN
      v_succeeded := v_succeeded + 1;
    ELSE
      v_failed := v_failed + 1;
    END IF;
  END LOOP;

  RETURN jsonb_build_object(
    'fetch_type', p_fetch_type,
    'dispatched', v_dispatched,
    'succeeded', v_succeeded,
    'failed', v_failed,
    'evaluated_at', v_now
  );
END;
$$;

DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM cron.job WHERE jobname = 'match-sync-events') THEN
    PERFORM cron.unschedule('match-sync-events');
  END IF;

  IF EXISTS (SELECT 1 FROM cron.job WHERE jobname = 'match-sync-odds') THEN
    PERFORM cron.unschedule('match-sync-odds');
  END IF;

  PERFORM cron.schedule(
    'match-sync-events',
    '*/2 * * * *',
    $cron$select public.dispatch_match_sync_jobs('events', 12);$cron$
  );

  PERFORM cron.schedule(
    'match-sync-odds',
    '*/15 * * * *',
    $cron$select public.dispatch_match_sync_jobs('odds', 24);$cron$
  );
END
$$;
